/*Создайте структуру типа date, содержащую три поля типа int: месяц, 
день и год. Попросите пользователя ввести день, месяц и год в формате 
31/12/2002, сохраните введенное значение в структурной переменной, а за- 
тем извлеките данные из этой переменной и выведите их на экран в том же 
формате, в каком они вводились. */
#include <iostream>
using namespace std;

struct date {
    int year;
    int mon;
    int day;
};

int main()
{
    setlocale(LC_ALL, "rus");
    date n;
    do {
        cout << "Введите день (1-31): ";
        cin >> n.day;
        if (n.day < 1 || n.day > 31) {
            cout << "Ошибка: день должен быть в диапазоне от 1 до 31.\n";
        }
    } while (n.day < 1 || n.day > 31);

    do {
        cout << "Введите месяц (1-12): ";
        cin >> n.mon;
        if (n.mon < 1 || n.mon > 12) {
            cout << "Ошибка: месяц должен быть в диапазоне от 1 до 12.\n";
        }
    } while (n.mon < 1 || n.mon > 12);

    cout << "Введите год: ";
    cin >> n.year;

    cout << "Вы ввели дату: "
        << (n.day < 10 ? "0" : "") << n.day << "/"
        << (n.mon < 10 ? "0" : "") << n.mon << "/"
        << n.year << endl;

    return 0;
}
